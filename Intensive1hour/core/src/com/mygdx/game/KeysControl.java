package com.mygdx.game;

public class KeysControl {
    public int kLeft;
    public int kRight;
    public int kUp;
    public int kDown;
    public int kFire;

    public KeysControl(int kLeft, int kRight, int kUp, int kDown, int kFire) {
        this.kLeft = kLeft;
        this.kRight = kRight;
        this.kUp = kUp;
        this.kDown = kDown;
        this.kFire = kFire;
    }
}
